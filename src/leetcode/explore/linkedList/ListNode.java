package leetcode.explore.linkedList;

/**
 * @author Pushy
 * @since 2019/1/4 19:05
 */
public class ListNode {

    public int val;
    public ListNode next;

    public ListNode(int val) {
        this.val = val;
    }

    @Override
    public String toString() {
        return "ListNode{" +
                "val=" + val +
                '}';
    }
}
